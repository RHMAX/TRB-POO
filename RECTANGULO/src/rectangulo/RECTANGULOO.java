    private void txtCALCULARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtCALCULARActionPerformed
       
        double area,perimetro,base,altura;
        
        altura=Double.parseDouble(String.valueOf(txtALTURA.getText()));
        base=Double.parseDouble(String.valueOf(txtBASE.getText()));
        area=base*altura;
        perimetro=(Math.pow(base, 2)+Math.pow(altura, 2));
        
        txtAREA.setText(String.valueOf(area));
        txtPERIMETRO.setText(String.valueOf(perimetro));
        
        
        
        
        
        
    }//GEN-LAST:event_txtCALCULARActionPerformed

